
package br.com.projeto_petshop.dto;

public class AnimalDTO {
    private String nome_animal, especie_animal, sexo_animal, raca_animal, porte_animal, cor_animal, pelagem_animal;
    private int id_animal;

    public String getNome_animal() {
        return nome_animal;
    }

    public void setNome_animal(String nome_animal) {
        this.nome_animal = nome_animal;
    }

    public String getEspecie_animal() {
        return especie_animal;
    }

    public void setEspecie_animal(String especie_animal) {
        this.especie_animal = especie_animal;
    }

    public String getSexo_animal() {
        return sexo_animal;
    }

    public void setSexo_animal(String sexo_animal) {
        this.sexo_animal = sexo_animal;
    }

    public String getRaca_animal() {
        return raca_animal;
    }

    public void setRaca_animal(String raca_animal) {
        this.raca_animal = raca_animal;
    }

    public String getPorte_animal() {
        return porte_animal;
    }

    public void setPorte_animal(String porte_animal) {
        this.porte_animal = porte_animal;
    }

    public String getCor_animal() {
        return cor_animal;
    }

    public void setCor_animal(String cor_animal) {
        this.cor_animal = cor_animal;
    }

    public String getPelagem_animal() {
        return pelagem_animal;
    }

    public void setPelagem_animal(String pelagem_animal) {
        this.pelagem_animal = pelagem_animal;
    }

    public int getId_animal() {
        return id_animal;
    }

    public void setId_animal(int id_animal) {
        this.id_animal = id_animal;
    }
    
}
